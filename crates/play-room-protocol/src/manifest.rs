pub fn typescript_constants_module() -> String {
    let manifest = protocol_manifest();
    format!(
        "// This file is generated by play-room-protocol. Do not edit by hand.\n\
         // Regenerate with: cargo run -p play-room-protocol --bin generate-web-protocol\n\n\
         export const PROTOCOL_VERSION = {protocol_version} as const;\n\n\
         export const PROTOCOL_VALUES = {{\n\
         {server_message_kinds}\n\
         {client_request_types}\n\
         {server_result_statuses}\n\
         {server_event_types}\n\
         {room_event_types}\n\
         {room_phase_types}\n\
         {game_kinds}\n\
         {moves}\n\
         {rps_moves}\n\
         {player_roles}\n\
         {error_codes}\n\
         {round_end_reasons}\n\
         {round_outcomes}\
         }} as const;\n",
        protocol_version = manifest.protocol_version,
        server_message_kinds = ts_field("server_message_kinds", &manifest.server_message_kinds),
        client_request_types = ts_field("client_request_types", &manifest.client_request_types),
        server_result_statuses =
            ts_field("server_result_statuses", &manifest.server_result_statuses),
        server_event_types = ts_field("server_event_types", &manifest.server_event_types),
        room_event_types = ts_field("room_event_types", &manifest.room_event_types),
        room_phase_types = ts_field("room_phase_types", &manifest.room_phase_types),
        game_kinds = ts_field("game_kinds", &manifest.game_kinds),
        moves = ts_field("moves", &manifest.moves),
        rps_moves = ts_field("rps_moves", &manifest.rps_moves),
        player_roles = ts_field("player_roles", &manifest.player_roles),
        error_codes = ts_field("error_codes", &manifest.error_codes),
        round_end_reasons = ts_field("round_end_reasons", &manifest.round_end_reasons),
        round_outcomes = ts_field_last("round_outcomes", &manifest.round_outcomes),
    )
}

fn ts_field(name: &str, values: &[String]) -> String {
    format!("  {name}: {},\n", ts_array(values))
}

fn ts_field_last(name: &str, values: &[String]) -> String {
    format!("  {name}: {}\n", ts_array(values))
}

fn ts_array(values: &[String]) -> String {
    let encoded = values
        .iter()
        .map(|value| serde_json::to_string(value).expect("protocol string should serialize"))
        .collect::<Vec<_>>()
        .join(", ");
    format!("[{encoded}]")
}

