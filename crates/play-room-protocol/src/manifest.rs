#[cfg(test)]
mod tests {
    use super::*;
    use std::{fs, path::Path};

    #[test]
    fn generated_web_protocol_constants_are_current() {
        let repo_root = Path::new(env!("CARGO_MANIFEST_DIR")).join("../..");
        let generated_path = repo_root.join("web/src/lib/protocol/generated.ts");
        let generated = fs::read_to_string(&generated_path)
            .unwrap_or_else(|err| panic!("failed to read {}: {err}", generated_path.display()))
            .replace("\r\n", "\n");

        assert_eq!(generated, typescript_constants_module());
    }

    #[test]
    fn manifest_values_match_protocol_serialization() {
        let manifest = protocol_manifest();

        assert_eq!(manifest.protocol_version, PROTOCOL_VERSION);
        assert_eq!(manifest.server_message_kinds, vec!["response", "event"]);
        assert!(manifest
            .client_request_types
            .contains(&"connect".to_owned()));
        assert!(manifest
            .error_codes
            .contains(&"client_limit_reached".to_owned()));
        assert!(manifest
            .room_event_types
            .contains(&"move_accepted".to_owned()));
    }
}
