fn simple_values<T: Serialize>(values: &[T]) -> Vec<String> {
    values.iter().map(simple_value).collect()
}

fn simple_value<T: Serialize>(value: &T) -> String {
    match serde_json::to_value(value).expect("protocol value should serialize") {
        Value::String(value) => value,
        other => panic!("expected string protocol value, got {other}"),
    }
}

fn tagged_value<T: Serialize>(value: &T, tag: &str) -> String {
    match serde_json::to_value(value).expect("protocol value should serialize") {
        Value::Object(mut object) => match object.remove(tag) {
            Some(Value::String(value)) => value,
            Some(other) => panic!("expected string field {tag}, got {other}"),
            None => panic!("missing protocol tag field {tag}"),
        },
        other => panic!("expected object protocol value, got {other}"),
    }
}

fn enum_or_object_tag<T: Serialize>(value: &T) -> String {
    match serde_json::to_value(value).expect("protocol value should serialize") {
        Value::String(value) => value,
        Value::Object(object) if object.len() == 1 => object
            .keys()
            .next()
            .expect("one-key protocol object should have a key")
            .to_owned(),
        other => panic!("expected string or one-key object protocol value, got {other}"),
    }
}

