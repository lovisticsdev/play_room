fn ts_field(name: &str, values: &[String]) -> String {
    format!("  {name}: {},\n", ts_array(values))
}

fn ts_field_last(name: &str, values: &[String]) -> String {
    format!("  {name}: {}\n", ts_array(values))
}

fn ts_array(values: &[String]) -> String {
    let encoded = values
        .iter()
        .map(|value| serde_json::to_string(value).expect("protocol string should serialize"))
        .collect::<Vec<_>>()
        .join(", ");
    format!("[{encoded}]")
}

